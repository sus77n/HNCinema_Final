    public void addFilm() {
        panelFilms.removeAll();
        for (int i = 0; i < Login.user.getListFavorite().size(); i++) {
            Film f = Login.user.getListFavorite().get(i);
            cardFilm cf = new cardFilm();
            cf.setData(f);
            panelFilms.add(cf);
            panelFilms.revalidate();
        }
    }

